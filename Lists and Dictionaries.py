# Example of a list
fruits = ["apple", "banana", "cherry"]
print(fruits[0])  # Access the first item in the list

# Loop through a list
for fruit in fruits:
    print(fruit)

# Example of a dictionary
student = {
    "name": "Vivek",
    "age": 21,
    "major": "Computer Science"
}

print(student["name"])  # Access the value using the key
print(student["age"])
